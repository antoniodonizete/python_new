# Tipos int e float
# int --> Número inteiro
# O tipo int representa qualquer número positivo ou
# negativo. int sem sinal é considerado positivo.
print(11) # int
print(-11) # int
print(0) # int

# float --> Número com ponto flutuante
# positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo.
print(1.1) # float
print(-1.1) # float

# A função type mostra o tipo que o Python
# inferiu valor.
print(   type('texto')       )
print(   type(1)       )
print(   type(-1.1), type('olá')       )